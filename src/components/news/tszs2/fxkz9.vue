            <p class="new_title">风险控制措施建议</p>
            <p>根据对移动接入风险的分析，针对攻击树中的威胁，建议从技术、制度及管理三个维度制定不同措施对风险进行消减。</p>
            <p>1.探索多样化技术手段规避安全风险。现阶段总行下发的一体化安全管理软件实现了内联网终端的统一管控，取得了良好的效果。在此基础之上，可探索在终端及网络出口处部署DLP(数据泄露防护系统)，防止业务网特定数据或信息以违反安全策略规定的形式流出，以更细致的颗粒度保证内网数据安全。探索业务网加密体系，利用非对称加密的灵活性及对称加密的高效率对业务网内所有数据进行加密，实现只有经认证的业务网网络设备才能接收数据流量，数据流量只有在经认证的业务网终端才能解密。</p>
            <p>2.构建体系化管理机制减轻安全风险。有效的安全管理应当通过自上而下的策略、标准、指南及基线等建立可靠有效的安全管理机制。对于移动办公的安全管理首先应由网络安全管理领导小组制定详尽的策略，对移动办公的使用范围、要求等要素进行定义和约束；其次应由科技部门指定相应标准，对实现移动办公技术中的技术标准及相关参数做出明确定义，确保在全行范围内以标准化的方式实施和使用移动办公技术。同时科技部门应制定通俗易懂的指南，确保用户能够充分理解移动办公平台的使用方式及不当使用所带来的风险。最后在移动办公平台经测试投入生产后由科技部门负责对基线进行快照，并定期将平台运行状况与基线进行对比，以确保能够及时发现安全隐患。</p>
            <p>3.强化应急管理控制安全风险。当移动办公平台发生安全事件时，应有明确的应急操作规程以控制安全风险的蔓延，其中应定义在事件发生过程中及事件发生后的处置步骤，譬如如何切断移动办公平台与业务网的连接、如何重置移动办公平台等流程。同时应定期进行演练，通过演练不断发现操作规程中的不足，输出反馈并及时修正应急操作规程，形成管理闭环。</p>

