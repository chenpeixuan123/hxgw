            <p class="new_title">背景介绍</p>
            <p>2019年10月24日，中共中央政治局就区块链技术发展现状和趋势进行第十八次集体学习。中共中央总书记习近平强调，要发挥区块链在促进数据共享、优化业务流程、降低运营成本、提升协同效率、建设可信体系等方面的作用。</p>
            <p>区块链之所以能够在几经浮沉后上升到国家战略层面与其自身的技术特性和应用价值是分不开的。</p>
            <p>首先，区块链最根本的就是共识机制，即分布式网络中所有节点就交易达成统一协议的过程。共识机制下数据共享不再是简单的搬运，而是针对要求更高的资金、权益等方面的价值数据实现价值互联和价值流动。共识机制可确保数字世界资金、权益可以自治循环流通，还可确保数据真实可信、共享的过程可控不可篡改、可追溯。这是区块链最核心的价值。</p>
