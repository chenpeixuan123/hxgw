            <p class="new_center">能源网：电力改革推动特高压建设</p>
            <p class="new_title">4.1 特高压技术特点</p>
            <p>输电过程中，电线由于有电阻，因此其消耗的电能随着输送能量（准确说是通过导线的有功电流）不断增大、传输距离的不断增加，而造成一次能源和经济上的损失，因此，为了提升电网的输电能力和效率水平，全球电网的电压等级也在持续提高。目前，在超远距离、超大规模电力传输方面，特高压技术是最为理想的解决方案，也是技术水平最高的领域。</p>
            <p>从定义来看，所谓特高压，是指应用在电压等级在交流1000Kv以上、直流±800Kv以上的输电技术，具有输送容量大、传输距离远、传输效率高和网络损耗低等优势。</p>
            <img src="http://images.gshxkj.com.cn:8090/news/qjzw/qjzw7.jpg" style="width: 100%">
            <p>在相同的输送功率下，1000Kv特高压交流输电线路最远的输送距离是500Kv交流线路的4倍。采用±800Kv直流输电技术可实现2500公里以上的经济输电距离，大大提升远距离输电的效率。</p>
            <p>线路损耗方面，1000Kv交流线路的网络损耗是500Kv的四分之一，±800Kv直流的线路损耗是±500Kv直流的40%，是±620Kv的60%。</p>
            <p>造价方面，1000Kv交流线路单位输送容量的综合造价是500Kv交流线路的四分之三，±800Kv直流线路的单位输送容量综合造价也是±500Kv直流线路的四分之三。</p>
            <p>在此前和很长一段时间内，电力传输一直采用交流的方式。随着电压传输等级的提升以及电力电子技术的不断发展，直流输电凭借特有的优势，也开始崭露头角。具体到特高压而言，直流特高压主要用于1000公里以上长距离、大功率、点对点输电方面，例如西南地区的水电外送；交流特高压相对而言输送容量较小，但投资较低，同时可以在沿线引出分路，当前主要用于600-800公里距离的大功率火电输送、三华联网、区域间电网异步互联和海底电缆送电等领域。</p>
            <p>特高压直流输电虽然输送容量较大，并且可以实现长距离非同步并网，但是当前其换流站成本非常高昂，同时控制系统较为复杂，因此并不具备成为电力系统骨干网络的条件。特高压直流输电更适合用于不同区域间电网的链接，以及远距离大容量的电力输送。而特高压交流系统则更适合作为大片区电力系统的中枢和骨干。整体来看，二者的技术并不存在优劣之分，应当优势互补，合理分工。</p>
            <p>对于我国而言，一个可靠、高效的电力网络应当以1000Kv级别的交流特高压输电线路为主形成区域内骨干网络，实现一个大范围区域，诸如三华、东北、西北等区域内的同步互联；以±800Kv级别特高压直流输电线路为主形成远距离、点对点的大功率区域间异步互联。</p>
            <p class="new_title">4.2 特高压市场空间</p>
            <p>3月初，国家电网有限公司印发了2020年改革攻坚重点工作安排《国家电网体改(2020) 8号》（8号文），其中第一条就提到了特高压，可见其今年将成为电网建设的重点领域。</p>
            <p>8号文指出：“在更高层面更大范围实施混合所有制改革。力争完成青海-河南特高压直流工程引战工作，积极征集白鹤滩-浙江特高压直流工程意向投资者。基本完成电动汽车、国康集团、通航公司、新源公司等二级单位混改。在信息通信、综合能源等新兴业务领域扩大混改实施范围。年内完成国网智能在科创板上市。上半年完成首批金融资产上市。国家部委确定的7家双百企业和4家混改试点企业改革取得标志性成果。选取浙江送变电公司等部分施工设计单位推进股权多元化改革。研究探索跨区输电、抽水蓄能、国际业务上市可行性。引资本与转机制并重，建立混改企业更加贴近市场的激励约束机制。”</p>
            <img src="http://images.gshxkj.com.cn:8090/news/qjzw/qjzw8.jpg" style="width: 100%">
            <p>目前我国正处于第二轮特高压建设批准高峰，主要项目是能源局2018年9月发布的《关于加快推进一批输变电重点工程规划建设工作的通知》中提到的多项特高压工程（“七交五直”+2条其他直流线路），整体投资规模预计在2000亿元左右。</p>
            <p>国家对于特高压的大规模投入，受益的不仅仅是特高压项目本身，也对上下游相关产业链的带动会起到立竿见影的作用。</p>
            <p>从宏观方面来看，青海-河南±800千伏特高压直流输电工程为例，该工程将直接拉动电源等相关产业投资超过2000亿元，增加7000多个就业岗位。陕北-湖北的±800千伏特高压直流工程，其总投资金额是185亿元，预计可直接带动设备产业规模120亿元以上，带动电源等相关产业投资超过700亿元，增加就业岗位超过4000个。</p>
            <p>从行业自身方面看，特高压产业链包括线缆、变压设备、高压开关、电源材料，机械等相关产业的发展，而且带动能力极强。特高压其关键技术除了高压电气开关设备、换流阀、电力电子、新材料等装备制造之外，依托5G信息技术可以推动物联网、芯片，软件及人工智能等高新技术的发展，符合我国当前的产业转换与升级。而且单个特高压项目的造价很高，由此还可以拉动装备制造、技术服务、建设安装等多领域的业绩增长。在此背景之下，特高压有望构建一个全新和行业生态。</p>

