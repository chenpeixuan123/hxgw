            <p class="new_center">逆周期调控需进一步加强对新基建投资的支持</p>
            <p>自2018年底，“宏观政策逆周期调节”的提法频繁出现于高层会议中，2019年中央经济工作会议提出“必须科学稳健把握逆周期调节力度”，适时适度逆周期调节将成为贯穿2020年宏观政策的主线。经济周期变化决定着政策的逆周期调节方向。由于经济运行存在繁荣、衰退、萧条和复苏的波动周期，在经济过热时采取收缩政策，而在经济下行时采取扩张措施，通过制定与经济周期逆向的财政、货币、金融监管等政策，政府可运用“有形之手”熨平经济运行过程中的周期性波动，并降低由此积累的系统性风险。2019年我国名义GDP为99.1万亿，全社会固定资产投资中的基建投资为18.5万亿。由于受疫情影响，市场对2020年名义GDP增速预期下调至7%，因此可以推算出2020年基建投资应为19.8万亿，基建投资增速为7.5%。2019年现实的基建投资增速为3.3%，仅为预估值的一半。考虑到今年受疫情影响，消费领域对经济增长的贡献度将会下降，出口形势同样不容乐观，因此基建在逆周期调控、稳定经济增长中的作用将会进一步凸显，动态来看，基建投资应有更高的增速才能达到稳增长的要求。</p>
            <p>去年以来，地方专项债在基建投资领域使用的灵活性不断增加，包括允许将专项债可作为项目资本金的使用领域进一步扩大，降低部分基础设施项目最低资本金比例，明确通过发行权益型、股权类金融工具筹措资本金的条件等。但是，整体来看，基建融资整体仍然偏紧，以城投债发行为例，2019年城投债净融资额3万亿左右，仅高于2018年金融严监管期间，显著低于2016-2017年的规模。因此，在现行的投融资机制下，传统基建整体增速回升的空间不宜高估，新基建投资则增长空间巨大。如果需要基建对冲疫情的经济影响，未来在基建投融资机制上要有进一步的放宽，尤其是对新基建的支持。</p>
            <p>从商业银行角度看，新基建给商业银行的传统信贷投放带来巨大机会。此外，各地提前下达的专项债中大部分为基建领域注入资金，这对于基建投资的政策支持，意味着未来几年专项债规模存在较大扩张空间，也利好于商业银行债券类业务创造增长机会。总体来看，商业银行在以新基建为代表的基础设施投资领域将大有可为。</p>

