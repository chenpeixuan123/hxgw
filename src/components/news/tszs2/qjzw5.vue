            <p class="new_title">3.2 5G建设投资规划与展望</p>
            <p class="new_title">（1）5G总体投资周期</p>
            <p>回顾3G/4G两代移动通信网络建设过程，3G自2007年试验网至今10年间累计投资5685亿元，4G自2012年试验网至今5年间累计投资7155亿元，增长25.85%。截止2019年底，三大运营商总共建设移动通信基站841万个，其中4G基站544万个。这还仅仅是三大运营商在3G/4G两代移动通信网络的投资总额，不包括其他衍生产业链及上下游规模。</p>
            <img src="http://images.gshxkj.com.cn:8090/news/qjzw/qjzw6.jpg" style="width: 100%">
            <p>由于5G高频段信号覆盖范围更小，5G建设基站将是4G的1.3-1.5倍；而由于多项新技术的应用，5G基站的成本也将是4G基站的1.2-1.3倍，因此，以基站为主的5G网络建设总投资规模会有更大幅度的增加。我们预计，5G网络达到基本覆盖并运行的状态，所需的总投资额在1.5万亿以上。</p>
            <p>5G网络建设投资会分为两大阶段：第一阶段5G网络建设投资周期大致从2019年到2022年，主要以满足传统上人与人之间的高速移动通信需求为主，主要投资逻辑与3G、4G相仿，但5G总投资规模都将有大幅度增加，投资周期更加集中，年均投资将明显高于4G。第二阶段5G网络建设投资大约将从2022开启，用以满足全新的物与物之间的移动通信需求，随着下游车联网、工业物联网等应用行业的成熟，毫米波基站、超密集光纤网络等满足物联网需求的基础设施建设将在2022年后逐步落地。同样，物联网信息基础设施的建成也将反过来推动更多新兴行业的诞生和发展，未来市场空间不可限量。</p>
