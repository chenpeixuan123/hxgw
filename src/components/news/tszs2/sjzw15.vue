            <p>近年来，随着智能移动终端设备的迅速普及与移动互联技术的成熟发展，移动应用逐渐成为金融业务最主要的渠道和入口。移动应用的更新迭代速度快、内容更趋个性化，银行客户对于细节和体验的要求更加严格和专业，从而对移动应用的测试提出了更高要求。鉴于此，中国工商银行提出了移动云测试平台的建设理念，通过自主研发的方式构建了具备云真机、自动化测试、兼容性测试、弱网络测试、安全测试等功能的私有云测试平台，推进前沿技术理念与质量保障工作的深度融合，为中国工商银行移动金融业务的快速发展提供了有力的科技保障。</p>
            <p class="new_title">自主创新，搭建平台解难题</p>
            <p>移动云测试平台项目自成功上线以来，面向全行服务，将智能手机、Pad等实体移动设备虚拟化为云上的资源。在此基础上，通过自动化、智能化的工具与服务，帮助开发者加大对移动应用缺陷探索的广度与深度，提升功能测试与非功能测试的效率，助力用户体验提升，解决了移动应用APP测试中面临的难题。平台主要功能如下。</p>
            
            <p>（1）云真机功能，支持用户通过浏览器访问并实时操作远端的实体移动设备，同时提供截图、录屏、性能数据采集、日志信息的抓取等辅助功能。</p>
            <p>（2）自动化测试功能，提供基于界面录制回放的自动化测试工具，录制所生成的脚本，还可在后续的版本测试中进行批量回放。</p>
            <p>（3）兼容性测试功能，提供多种兼容性测试方案，支持APP在不同型号手机上的安装/卸载测试、遍历测试、monkey测试等。</p>
            <p>（4）安全测试功能，帮助用户对APP进行静态安全扫描，一键即可自动完成相应的安全漏洞检查、合规检查、接口泄露风险检查等。</p>
            <p>（5）弱网络测试功能，通过模拟各类网络信号较差的环境，如地铁、电梯间的网络环境，帮助用户测试此环境下移动应用的运行稳定情况。</p>

            <p class="new_title">攻坚克难，关键技术勇突破</p>
            <p>1.软硬兼施，打牢基础框架。通过软硬件的协同开发与充分实验，移动云测试平台在项目建设之初即搭建了稳定的、易于维护的软硬件基础设施环境，具有多点接入、分布式部署、动态弹性扩展、集中智能管控等技术特点（见下图）。</p>
