            <p>当今世界面临着百年未有之大变局，新一轮科技革命和产业变革加速演进。如果说迅猛发展的IT技术，是这个时代最为重要的生产力，那么在技术驱动创新发展的过程中，质量管理无疑是软件工程这个“生产方式”的底线和根基。</p>
            <p>近年来，中国银行逐步实现了瀑布研发和敏捷研发的“双模”灵活切换，完成了支持“双模”过程管理和监控的质量管理体系建设，确保了业务价值的快速交付和组织级的提质增效。并以此为基础自2018年起，逐步启动了软件产品质量管理的数字化转型工作，旨在打造一套标准化、数字化的质量管理体系，建立全生命周期的质量管理机制，以“端到端、全流程、全员化、智能化”的管控方式和力度，全面夯实产品质量根基，更好地应对因快速的技术更迭和激烈的市场竞争所带来的机遇和挑战。</p>
            <p>中国银行应用系统研发的数字化质量管理体系围绕“112N-4”主体实施，即1个体系，1个平台，2个目标，N个应用及4个成效（见图1）。明确精细化、规范化和量化管理流程，打破开发、测试和运维条线间的壁垒，确保各个环节无缝衔接、各个角色高效协同。积极做好新技术预研，建立产品质量智能管理平台，实现质量数据智能管理和决策，多措并举提升所交付的软件产品质量。</p>
            <img src="@/assets/images/news/sskj.png" style="width: 60%" />
            <p class="new_title">顶层设计建设“1个体系”</p>
            <p>对中国银行，质量管理既是一种文化，更是一套体系，是大型组织产品质量的有力保障。作为全行软件研发的实施机构，中国银行软件中心在多年的实践与探索中深耕质量管理体系建设。质量体系建设起步于瀑布模型，经过国内和海外蓝图的不断锤炼，在日新月异的技术发展、敏捷迭代的新模式中持续建立并完善。</p>
            <p>在质量管理体系建设过程中，中国银行软件中心始终坚持端到端的理念，并且较早建立了开发、测试、运维相互融合的一体化机制，确保质量管理体系涵盖开发、测试、运维全生命周期。与此同时，时刻关注和洞察金融科技发展的新形势、新趋势，做到与时俱进、推陈出新，不断优化“1个体系”建设，逐步将其打造成为同时支持敏捷和传统研发模式的“双模”并行质量管理体系。</p>
            <p>作为一个系统完整的体系，“端到端”是中行质量体系的鲜明特征：</p>
            <p>一是软件研发过程中端到端“微循环”模式日渐成型。通过改进开发工艺，引入持续集成、持续交付和DevOps应用，形成串联起开发、测试、运维完整闭环的端到端、一体化交付流水线。以质量内建为主轴，设计50多个质量控制活动、40多个质量度量指标，涵盖瀑布、敏捷研发模式，贯穿研发实施的事前、事中和事后。其中，质量管理的策划及预测实现了事前预防；质量门的设置实现了事中风险的及时控制；多维度、多层次的事后质量评价为持续改进提供输入，从而优化端到端“微循环”效能。</p>
            <p>二是科技业务融合的正反馈“正循环”机制日趋成熟。端到端敏捷进一步加速了科技与业务的深度融合，并使得软件产品在走出行内、面向客户时，更敢于倾听关于“品质”的声音，从而形成从外部客户到行内业务条线再到技术实施条线的正反馈机制。软件实施过程依托DevOps交付流水线，将质量标准规范内建到产品的生产工艺，在里程碑点设立质量门，实现了质量标准统一化、质量数据实时化、质量评价和反馈及时化的管理闭环；同时兼顾质量管理的软规范和硬约束，在加速质量检测标准数字化建设的同时，全面提升软件产品质量，促进业务发展，提升客户体验。</p>
            <p>三是质量管理数字化体系生态“大循环”日臻完善。作为质量管理数字化体系的有机组成部分，内部“微循环”的成熟、稳定运转，也将会在软件产品的研发实施和业务营销等方面持续赋能，助力科技创新与现代金融间的良性循环。同时，商业银行对金融市场和金融服务品质所保持的敏锐嗅觉，也将对体系内部的“微循环”起到“反哺”效应，保“质”保“量”，全面提升软件产品的“核心硬实力”。</p>
            <p class="new_title">贯穿全局打造“1个平台”</p>
            <p>建立集质量与过程数据采集、数据处理、监控管理、预测预警和可视化为一体的质量智能管理平台，确保质量管理体系有效“落地”。平台具有下述三个特点。</p>
            <p>一是质量数据标准化。基于CMMI4、ISO9001、ISO27001等标准规范，结合我行长期以来在应用系统研发实施领域积累的经验，建立具有中行特色的质量管理体系数据标准，统筹规划各类质量监控和度量指标，建立统一的度量视图，实现全生命周期质量数据的标准化建设。</p>
            <p>二是质量分析数字化。多维度、全方位采集质量数据，采用在线准实时计算，对所采集数据进行深入分析和挖掘，对相关问题和瓶颈进行重点突破，助力研发、测试及运维人员实时跟踪过程信息，有效提高开发、测试和运维效率，切实提升产品交付过程的工艺水平。</p>
            <p>三是质量管理智能化。基于机器学习、深度学习、运筹优化等技术，对不同业务领域的质量过程数据进行模型训练、AI预测，打造开放、轻量的能力共享中心。实现对质量管理工作的AI预测、决策和调度，解决软件实施过程中的质量保证和质量控制难点，全面提升质量管理工作的“智控”水平（见图2）。</p>
