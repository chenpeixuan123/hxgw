            <p>一方面，全链条嵌入DevOps一体化平台，让质量管理的内涵更加丰富。DevOps一体化平台具备门户统一、灵活配置、可扩展、支持多工具接入和集成的功能。在DevOps交付流水线各工程环节嵌入质量验证标准、各类研发测试工具，以及自动化测试和自动化监控工具，以全面提升质量实施效率。此外，整合多路DevOps交付流水线，为研发人员提供统一的基础研发测试平台。</p>
            <p>另一方面，全方位开展自动化测试，让质量管理的效率几何级增长。在接口测试、稳定性测试、云平台升级、敏捷迭代和回归测试中，持续提升组织级的自动化能力，效果显著。集成组装测试、验收测试所有自动化测试工具，实现中心自动化测试工具统一管理与调度。打造中心级的自动化测试执行中心与监控中心，实现测试自动执行、测试结果自动记录和比对，不断向“无人值守”的测试的目标前进。持续优化自动化部署平台，实现应用系统全过程自动化部署。</p>
            <p>此外，聚焦开源软件、制品管理系统建设，配套建设新一代配置管理系统，有效支持分布式架构，完善持续交付基础设施，保障应用软件资产安全受控。依托产品质量智能分析平台对质量趋势进行提示和预警，建立质量预测模型，实现“用数据说话、用数据管理、用数据预测”；最终实现产品研发过程质量和风险的全方位预测、监控、防范和干预，保障产品研发过程的高效协作。</p>
            <p class="new_title">灵活敏捷支持“N个应用”</p>
            <p>质量智能管理平台开放灵活，支持“多个应用”。目前已实现的应用场景覆盖产品研发的多个过程，尤其是测试环节，兼顾问题排查的广度和问题定位的精度，实现“全链路、地毯式”梳理，进一步加大产品质量管控力度。</p>
            <p>一是测试覆盖度分析。从应用系统、测试环境两个维度展示输入和输出的接口数据，实现接口覆盖统计和接口详情查询，保证接口测试的覆盖度和完整性。并能够基于下发产品版本，提供每日代码覆盖率、波动性等各类指标，助力精准跟踪业务数据流向，使研发测试人员对当前测试过程完备性“知己知彼”，既保证质量又规避风险。</p>
            <p>二是问题精准定位。首先能够对联机和批量运行的整体情况进行监控，为开发、测试、运维人员等不同角色，提供详尽的各类数据及分析结果。其次能够通过详细的错误信息列表和日志查阅，实现问题精准定位。另外可以广泛收集历史错误信息，并智能做出分析预判，对可能出现风险的功能、环节、模块进行重点关注和测试，进一步优化测试资源的分配和使用。</p>
            <p>三是提升分行特色质量。中国银行业务全球化特点鲜明，为了满足境内外特色业务和复杂的监管要求，通过采集、分析分行产品接口全景以及调用关系，形成统一基线，能够根据接口变更情况自动识别分行接口测试范围，并根据接口测试覆盖情况进行实时预警；通过平台实现采集测试日志，自动比对接口测试覆盖情况，实现分行特色系统接口测试的可视化、自动化的管理手段，做实总分行应用系统测试工作的“最后一公里”。</p>
            <p>四是赋能研发质量提升。通过采集技术评审阶段的技术评审缺陷、开发阶段的代码复查缺陷分析、测试阶段的测试缺陷和产品生产缺陷等数据，经过智能分析对质量趋势进行提示和预警，并进一步识别质量风险和改进机会，实现了对研发过程质量风险的有效定位和精准改进。另外，通过积累和建立过程能力基线，进行缺陷量化管理，从而提高产品质量风险的提前识别、控制能力，为及时采取质量干预措施提供决策依据。</p>
            <p class="new_title">硕果显现取得“4项成效”</p>
            <p>完善的数字化质量管理体系和高效的质量智能管理平台建设，将为未来全行产品开发、测试和运维工作探索出高“质量”的发展方向，夯实高“质量”发展根基。成效主要体现在四个方面：一是沉淀质量数据。以数据为驱动，采集产品研发各阶段过程质量数据，建立基础质量大数据池，为数字化转型奠定数据基础。二是构建质量画像。以量化为手段，以质量度量管理体系为依托，制定产品研发过程质量监控指标，实现事中实时监控告警、事后分析评估。三是赋能管理决策。依托质量画像，全面度量从需求到用户全生命周期的质量水平，聚焦过程性能、交付缺陷率等关键指标，管控质量风险和实现智能决策，不断改进管理效能。四是促进价值创造。在产品持续优化完善的过程中，从用户视角统筹兼顾功能、性能与体验等质量指标，持续迭代融入新一轮需求与产品设计，促进金融科技产品价值提升。</p>
            <p>质量是企业的生命线。在金融科技成为服务实体经济、金融供给侧改革重要抓手的今天，中国银行将更加关注研发过程中的质量管理。继续在数字化、智能化领域深耕细作，坚守合规发展底线，聚焦价值创造，以“高可用、高质量、高安全”的科技产品，加速科技业务深度融合，实现科技对业务发展的“全链路”赋能，为全行数字化发展持续贡献力量。</p>

